#Desafio 7 - Desenvolver programa para ler duas notas de aluno e calcular média
n1 = float(input('primeira nota: '))
n2 = float(input('segunda nota: '))
m = (n1 + n2) / 2
print('a nota do aluno 1 foi {:.2f} ,e a do aluno 2 foi {:.2f}. Portanto, a média das duas notas foi{:.2f}'.format(n1,n2,m))
